#include <stdio.h>

 int main() {

    /*
    Incremento (++)
    Pré-Incremento ++A
    Pós-Incremento A++
    Decremento (--)
    Pré-Decremento --A
    Pós-Decremento A--
    */

    int numero1 = 1;

    printf("Antes Incremento: %d\n", numero1);
    numero1++;
    printf("Após Incremento: %d\n", numero1);


 }
 